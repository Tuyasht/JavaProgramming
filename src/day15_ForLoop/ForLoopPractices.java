package day15_ForLoop;

public class ForLoopPractices {

    public static void main(String[] args) {

        for(int i = 100; i >= 50; i--){
            System.out.print(i+" ");
        }
        System.out.println();
        System.out.println("--------------------------------------------------");

       // print all the even number between 1 ~ 55


    }

}
