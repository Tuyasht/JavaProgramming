public class CastingExp {
    public static void main(String[] args) {

        // double > float > long > int > short > byte

        double num = 12; //double is greater than int we have to re-assign to int by explicit casting done by devs
        int num2 = (int)num; //explicit casting done by developers

         short num4 = 34;
         int num5 = num4; // implicit casting done by compiler




    }
}
